//! ENDF MAT (material) number lookup.
//!
//! MAT numbers are the ENDF material identifiers used to retrieve evaluated
//! nuclear data files. This table covers 535 ground-state isotopes from the
//! ENDF/B-VIII.0 neutrons sublibrary.
//!
//! Data source: PLEIADES `neutrons.list` (ENDF/B-VIII.0), metastable states excluded.

/// Look up the ENDF MAT number for a ground-state isotope (Z, A).
///
/// Returns `None` if the isotope is not in the ENDF/B-VIII.0 neutrons sublibrary.
///
/// # Examples
/// ```
/// assert_eq!(endf_mat::mat_number(92, 235), Some(9228));
/// assert_eq!(endf_mat::mat_number(92, 238), Some(9237));
/// assert_eq!(endf_mat::mat_number(26, 56), Some(2631));
/// ```
pub fn mat_number(z: u32, a: u32) -> Option<u32> {
    // Binary search on (Z, A) key. Table is sorted by Z then A.
    ENDF_MAT_TABLE
        .binary_search_by(|&(tz, ta, _)| (tz, ta).cmp(&(z, a)))
        .ok()
        .map(|idx| ENDF_MAT_TABLE[idx].2)
}

/// Reverse lookup: MAT number → (Z, A).
///
/// Returns `None` if the MAT number is not in the table.
///
/// # Examples
/// ```
/// assert_eq!(endf_mat::isotope_from_mat(9228), Some((92, 235)));
/// assert_eq!(endf_mat::isotope_from_mat(9999), None);
/// ```
pub fn isotope_from_mat(mat: u32) -> Option<(u32, u32)> {
    // Linear scan — reverse lookups are rare.
    ENDF_MAT_TABLE
        .iter()
        .find(|&&(_, _, m)| m == mat)
        .map(|&(z, a, _)| (z, a))
}

/// All mass numbers with ENDF/B-VIII.0 evaluations for element Z.
///
/// Returns every A value in the MAT table for the given atomic number Z,
/// including synthetic and transuranic isotopes. Returns an empty `Vec`
/// for elements with no evaluations.
///
/// # Examples
/// ```
/// let pu = endf_mat::known_isotopes(94);
/// assert!(pu.contains(&239));
///
/// let tc = endf_mat::known_isotopes(43);
/// assert_eq!(tc, vec![98, 99]);
/// ```
pub fn known_isotopes(z: u32) -> Vec<u32> {
    let start = ENDF_MAT_TABLE.partition_point(|&(tz, _, _)| tz < z);
    ENDF_MAT_TABLE[start..]
        .iter()
        .take_while(|&&(tz, _, _)| tz == z)
        .map(|&(_, a, _)| a)
        .collect()
}

/// Whether the ENDF/B-VIII.0 neutrons sublibrary has an evaluation for (Z, A).
///
/// # Examples
/// ```
/// assert!(endf_mat::has_endf_evaluation(94, 239));  // Pu-239
/// assert!(!endf_mat::has_endf_evaluation(94, 999)); // no such isotope
/// ```
pub fn has_endf_evaluation(z: u32, a: u32) -> bool {
    mat_number(z, a).is_some()
}

