#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_mat_number_known_values() {
        assert_eq!(mat_number(92, 235), Some(9228));
        assert_eq!(mat_number(92, 238), Some(9237));
        assert_eq!(mat_number(26, 56), Some(2631));
        assert_eq!(mat_number(1, 1), Some(125));
        assert_eq!(mat_number(0, 1), Some(25)); // neutron
        assert_eq!(mat_number(94, 239), Some(9437));
    }

    #[test]
    fn test_mat_number_tungsten() {
        assert_eq!(mat_number(74, 182), Some(7431));
        assert_eq!(mat_number(74, 183), Some(7434));
        assert_eq!(mat_number(74, 184), Some(7437));
        assert_eq!(mat_number(74, 186), Some(7443));
    }

    #[test]
    fn test_mat_number_tin_expansion() {
        // Tin isotopes — NOT in the old 47-entry table, proves expansion works
        assert_eq!(mat_number(50, 120), Some(5049));
        assert_eq!(mat_number(50, 116), Some(5037));
        assert_eq!(mat_number(50, 118), Some(5043));
    }

    #[test]
    fn test_mat_number_corrected_regressions() {
        // Regression tests for isotopes whose MAT values were incorrect in the
        // old hand-coded 47-entry table (off by one isotope offset each).
        // Correct values sourced from PLEIADES neutrons.list (ENDF/B-VIII.0).

        // Cd-113: old table had 4849 (actually Cd-114), correct is 4846
        assert_eq!(mat_number(48, 113), Some(4846));
        assert_eq!(isotope_from_mat(4846), Some((48, 113)));

        // Hf-177: old table had 7231 (actually Hf-176), correct is 7234
        assert_eq!(mat_number(72, 177), Some(7234));
        assert_eq!(isotope_from_mat(7234), Some((72, 177)));

        // Hf-178: old table had 7234 (actually Hf-177), correct is 7237
        assert_eq!(mat_number(72, 178), Some(7237));
        assert_eq!(isotope_from_mat(7237), Some((72, 178)));

        // W-182/183/184/186: old table was shifted by one isotope
        assert_eq!(mat_number(74, 182), Some(7431));
        assert_eq!(mat_number(74, 183), Some(7434));
        assert_eq!(mat_number(74, 184), Some(7437));
        assert_eq!(mat_number(74, 186), Some(7443));
    }

    #[test]
    fn test_mat_number_unknown() {
        assert_eq!(mat_number(200, 400), None);
        assert_eq!(mat_number(0, 0), None);
    }

    #[test]
    fn test_isotope_from_mat() {
        assert_eq!(isotope_from_mat(9228), Some((92, 235)));
        assert_eq!(isotope_from_mat(9237), Some((92, 238)));
        assert_eq!(isotope_from_mat(2631), Some((26, 56)));
        assert_eq!(isotope_from_mat(25), Some((0, 1))); // neutron
    }

    #[test]
    fn test_isotope_from_mat_unknown() {
        assert_eq!(isotope_from_mat(9999), None);
        assert_eq!(isotope_from_mat(0), None);
    }

    #[test]
    fn test_mat_roundtrip() {
        for &(z, a, mat) in ENDF_MAT_TABLE.iter() {
            assert_eq!(
                mat_number(z, a),
                Some(mat),
                "mat_number({}, {}) failed",
                z,
                a
            );
            assert_eq!(
                isotope_from_mat(mat),
                Some((z, a)),
                "isotope_from_mat({}) failed",
                mat
            );
        }
    }

    #[test]
    fn test_table_size() {
        assert_eq!(ENDF_MAT_TABLE.len(), 535);
    }

    #[test]
    fn test_table_sorted() {
        for i in 1..ENDF_MAT_TABLE.len() {
            let (z1, a1, _) = ENDF_MAT_TABLE[i - 1];
            let (z2, a2, _) = ENDF_MAT_TABLE[i];
            assert!(
                (z1, a1) < (z2, a2),
                "Table not sorted at index {}: ({}, {}) >= ({}, {})",
                i,
                z1,
                a1,
                z2,
                a2
            );
        }
    }
}
