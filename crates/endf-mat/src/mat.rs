    #[test]
    fn test_tendl_shared_isotopes_agree_except_es255() {
        // For every (Z, A) in both tables, MATs should agree — except the documented
        // Es-255 mismatch. Regression guard: if the TENDL table is regenerated and a
        // new MAT diverges from ENDF/B-VIII.0, this test fires so we can decide
        // whether the new mismatch is intentional.
        for &(z, a, endfb_mat) in ENDF_MAT_TABLE {
            if let Some(tendl_mat) = mat_number_tendl(z, a) {
                if z == 99 && a == 255 {
                    continue; // documented mismatch
                }
                assert_eq!(
                    tendl_mat, endfb_mat,
                    "MAT mismatch for (Z={z}, A={a}): ENDF/B-VIII.0={endfb_mat}, TENDL-2023={tendl_mat}"
                );
            }
        }
    }
