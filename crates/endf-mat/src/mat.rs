/// Look up the TENDL-2023 MAT number for a ground-state isotope (Z, A).
///
/// TENDL-2023 covers ~2,300 ground-state isotopes (Z=1 H to Z=115 Mc),
/// substantially more than the ENDF/B-VIII.0 535 covered by [`mat_number`].
/// Returns `None` for isotopes outside the TENDL-2023 neutrons sublibrary.
///
/// MAT numbers are *almost* identical to ENDF/B-VIII.0 for shared isotopes,
/// with one known exception: Es-255 is MAT 9915 in TENDL-2023 vs. MAT 9916
/// in ENDF/B-VIII.0. Use this function (not [`mat_number`]) when constructing
/// TENDL-2023 retrieval URLs.
///
/// # Examples
/// ```
/// assert_eq!(endf_mat::mat_number_tendl(92, 235), Some(9228));
/// assert_eq!(endf_mat::mat_number_tendl(99, 255), Some(9915));  // TENDL-specific
/// assert_eq!(endf_mat::mat_number_tendl(100, 247), Some(9928)); // Fm-247: not in ENDF/B-VIII.0
/// ```
pub fn mat_number_tendl(z: u32, a: u32) -> Option<u32> {
    TENDL_2023_MAT_TABLE
        .binary_search_by(|&(tz, ta, _)| (tz, ta).cmp(&(z, a)))
        .ok()
        .map(|idx| TENDL_2023_MAT_TABLE[idx].2)
}

